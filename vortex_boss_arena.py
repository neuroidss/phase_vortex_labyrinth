def predict_squad_matchup(team0_actors, team1_actors):
    """
    Closed-form O(N) deterministic matchup prediction engine:
    Calculates Effective DPS, HPS, EHP, Time-To-Kill (TTK) and Attacker Win Probability
    taking the 90-second timeout rule strictly into account.
    """
    def calc_squad_stats(actors):
        edps = 0.0
        ehps = 0.0
        ehp = 0.0
        for a in actors:
            if getattr(a, 'is_dead', False): continue
            w_cfg = WEAPON_CONFIGS.get(a.weapon, {"dps_weight": 1.0, "hps_weight": 0.0})
            c_cfg = CREATURE_CONFIGS.get(a.creature, {"hp_mult": 1.0, "power_mult": 1.0})
            
            lvl_scale = (1.0 + a.level * 0.1) * (0.5 + a.quality / 100.0)
            
            unit_dps = w_cfg["dps_weight"] * COMBAT_BALANCE_CONFIG["DAMAGE_SCALE_BASE"] * 100.0 * c_cfg["power_mult"] * lvl_scale
            unit_hps = w_cfg["hps_weight"] * COMBAT_BALANCE_CONFIG["BASE_HEAL_RATE"] * 100.0 * c_cfg["power_mult"] * lvl_scale
            unit_hp = getattr(a, 'integrity', 1.0) * 100.0 * c_cfg["hp_mult"]
            
            edps += unit_dps
            ehps += unit_hps
            ehp += unit_hp
            
        return edps, ehps, ehp

    edps0, ehps0, ehp0 = calc_squad_stats(team0_actors)
    edps1, ehps1, ehp1 = calc_squad_stats(team1_actors)

    # Net damage output considering opposing healing
    net_dps0_to_1 = max(0.1, edps0 - ehps1 * 0.5)
    net_dps1_to_0 = max(0.1, edps1 - ehps0 * 0.5)

    ttk0_to_1 = ehp1 / net_dps0_to_1  # Seconds required for Team 0 to wipe Team 1
    ttk1_to_0 = ehp0 / net_dps1_to_0  # Seconds required for Team 1 to wipe Team 0

    match_limit = COMBAT_BALANCE_CONFIG["MATCH_DURATION_LIMIT"]

    # Attacker (Team 0) MUST destroy Defender (Team 1) before 90-second match limit
    if ttk0_to_1 > match_limit:
        win_prob_0 = max(0.0, 1.0 - (ttk0_to_1 - match_limit) / 30.0) * 0.15
    else:
        ttk_diff = ttk1_to_0 - ttk0_to_1
        scale = COMBAT_BALANCE_CONFIG["PREDICTION_SIGMOID_SCALE"]
        win_prob_0 = 1.0 / (1.0 + math.exp(-scale * ttk_diff))

    return {
        "team0_edps": edps0, "team0_ehps": ehps0, "team0_ehp": ehp0, "ttk_0_to_1": ttk0_to_1,
        "team1_edps": edps1, "team1_ehps": ehps1, "team1_ehp": ehp1, "ttk_1_to_0": ttk1_to_0,
        "win_probability_0": win_prob_0 * 100.0
    }

class StandaloneFluidSolver:
    def __init__(self, res, device):
        self.res = res
        self.device = device
        y, x = torch.meshgrid(
            torch.linspace(-1.0, 1.0, res, device=device),
            torch.linspace(-1.0, 1.0, res, device=device), indexing='ij'
        )
        self.grid_x = x
        self.grid_y = y

    def advect(self, field, u, v, dt):
        dx = u[0, 0] * (dt * 2.0 / self.res)
        dy = v[0, 0] * (dt * 2.0 / self.res)
        sampling_grid = torch.stack([self.grid_x - dx, self.grid_y - dy], dim=-1).unsqueeze(0)
        sampling_grid = torch.clamp(sampling_grid, -1.0, 1.0)
        return F.grid_sample(field, sampling_grid, mode='bilinear', padding_mode='zeros', align_corners=True)

    def compute_laplacian(self, field):
        """ Isotropic 9-point Laplacian stencil (Patra-Cartwright formulation) for O(dx^4) rotational invariance """
        field_pad = F.pad(field, (1, 1, 1, 1), mode='replicate')
        
        center = field
        c_sum = (field_pad[:, :, 1:-1, 2:] + field_pad[:, :, 1:-1, :-2] +
                 field_pad[:, :, 2:, 1:-1] + field_pad[:, :, :-2, 1:-1])
        
        d_sum = (field_pad[:, :, 2:, 2:] + field_pad[:, :, 2:, :-2] +
                 field_pad[:, :, :-2, 2:] + field_pad[:, :, :-2, :-2])
        
        return (4.0 * c_sum + 1.0 * d_sum - 20.0 * center) / 6.0

    def compute_gradient(self, field):
        """ Isotropic 9-point Scharr gradient operator for rotationally symmetric derivative field calculation """
        field_pad = F.pad(field, (1, 1, 1, 1), mode='replicate')
        t_left, t_mid, t_right = field_pad[:, :, :-2, :-2], field_pad[:, :, :-2, 1:-1], field_pad[:, :, :-2, 2:]
        m_left,        m_right = field_pad[:, :, 1:-1, :-2],                            field_pad[:, :, 1:-1, 2:]
        b_left, b_mid, b_right = field_pad[:, :, 2:, :-2],  field_pad[:, :, 2:, 1:-1],  field_pad[:, :, 2:, 2:]
        
        gx = (3.0 * (t_right - t_left) + 10.0 * (m_right - m_left) + 3.0 * (b_right - b_left)) / 32.0
        gy = (3.0 * (b_left - t_left) + 10.0 * (b_mid - t_mid) + 3.0 * (b_right - t_right)) / 32.0
        return gx, gy

    def project(self, u, v, wall_density):
        block_mask = (wall_density > 0.4).float()
        u = u * (1.0 - block_mask)
        v = v * (1.0 - block_mask)
        
        du_dx, _ = self.compute_gradient(u)
        _, dv_dy = self.compute_gradient(v)
        div = du_dx + dv_dy
        
        p = torch.zeros_like(u)
        for _ in range(10):
            p_pad = F.pad(p, (1, 1, 1, 1), mode='replicate')
            c_sum = (p_pad[:, :, 1:-1, 2:] + p_pad[:, :, 1:-1, :-2] + 
                     p_pad[:, :, 2:, 1:-1] + p_pad[:, :, :-2, 1:-1])
            d_sum = (p_pad[:, :, 2:, 2:] + p_pad[:, :, 2:, :-2] + 
                     p_pad[:, :, :-2, 2:] + p_pad[:, :, :-2, :-2])
            p = (4.0 * c_sum + 1.0 * d_sum - 6.0 * div) / 20.0
                    
        dp_dx, dp_dy = self.compute_gradient(p)
        u -= dp_dx
        v -= dp_dy
        
        return u * (1.0 - block_mask), v * (1.0 - block_mask)

