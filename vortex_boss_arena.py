    def fire_bow(self, source, target_pos):
        s_pos = source.pos.cpu().numpy()
        dir_x, dir_y = target_pos[0] - s_pos[0], target_pos[1] - s_pos[1]
        dist = math.hypot(dir_x, dir_y) + 1e-5
        dir_x, dir_y = float(dir_x / dist), float(dir_y / dist)
        
        inj_x, inj_y = s_pos[0] + dir_x * 55.0, s_pos[1] + dir_y * 55.0
        gx, gy = int((inj_x / ARENA_WIDTH) * COMPUTE_RES), int((inj_y / HEIGHT) * COMPUTE_RES)
        
        success = False
        freq = 60.0 if source.team == 0 else 65.0
        
        if 2 <= gx < COMPUTE_RES - 2 and 2 <= gy < COMPUTE_RES - 2:
            success = True
            for dy in range(-1, 2):
                for dx in range(-1, 2):
                    r_sq = dx*dx + dy*dy
                    weight = math.exp(-r_sq / 1.5)
                    self.density_spectral[0, int(freq), gy+dy, gx+dx] = torch.clamp(
                        self.density_spectral[0, int(freq), gy+dy, gx+dx] + weight * 2.1, 0.0, 2.2
                    )
                    self.u[0, 0, gy+dy, gx+dx] = float(dir_x * 900.0 * weight)
                    self.v[0, 0, gy+dy, gx+dx] = float(dir_y * 900.0 * weight)
                        
        source.current_action = "[SHOOTING]" if success else "[BLOCKED]"
        return success

    def fire_shuriken(self, source, target_pos):
        s_pos = source.pos.cpu().numpy()
        dir_x, dir_y = target_pos[0] - s_pos[0], target_pos[1] - s_pos[1]
        dist = math.hypot(dir_x, dir_y) + 1e-5
        dx, dy = float(dir_x / dist), float(dir_y / dist)
        
        success = False
        angles = [-0.42, 0.0, 0.42]
        freq = 40.0 if source.team == 0 else 45.0
        
        for a in angles:
            rx, ry = dx * math.cos(a) - dy * math.sin(a), dx * math.sin(a) + dy * math.cos(a)
            rx, ry = float(rx), float(ry)
            inj_x, inj_y = s_pos[0] + rx * 55.0, s_pos[1] + ry * 55.0
            gx, gy = int((inj_x / ARENA_WIDTH) * COMPUTE_RES), int((inj_y / HEIGHT) * COMPUTE_RES)
            
            if 2 <= gx < COMPUTE_RES - 2 and 2 <= gy < COMPUTE_RES - 2:
                success = True
                for dy_offset in range(-1, 2):
                    for dx_offset in range(-1, 2):
                        r_sq = dx_offset**2 + dy_offset**2
                        weight = math.exp(-r_sq / 1.5)
                        self.density_spectral[0, int(freq), gy+dy_offset, gx+dx_offset] = torch.clamp(
                            self.density_spectral[0, int(freq), gy+dy_offset, gx+dx_offset] + weight * 1.8, 0.0, 2.0
                        )
                        self.u[0, 0, gy+dy_offset, gx+dx_offset] = float(rx * 880.0 * weight)
                        self.v[0, 0, gy+dy_offset, gx+dx_offset] = float(ry * 880.0 * weight)
                        if r_sq > 0:
                            tx = -float(dy_offset) / math.sqrt(r_sq)
                            ty = float(dx_offset) / math.sqrt(r_sq)
                            self.u[0, 0, gy+dy_offset, gx+dx_offset] += float(tx * 600.0 * weight)
                            self.v[0, 0, gy+dy_offset, gx+dx_offset] += float(ty * 600.0 * weight)
        return success

